
public class TiposVariaveis {

	public static void main(String[] args) {

		int numero = 10;

		// String: textos (ex: "café", "escola", "Sandy Campos")
		String minhaVariavelString = "Sandy 10";

		// Variáveis números
		// byte: números de -128 até 127
		byte minhaVariavelByte = 20;

		// short: números de -32768 até 32767
		short minhaVariavelShort = 3200;

		// int: números de -2147483648 até 2147483647
		int minhaVariavelInt = 380000;

		// long: números de -9223372036854775808 até 9223372036854775807
		long minhaVariavelLong = 10000000000L;

		// float: números decimais com precisão de até 7 casas após a vírgula
		// float preco = 2.12345678f;

		// double: números decimais com precisão de até 15 a 16
		double preco = 2.1234567890123456;

		// decimal: precisão de 28 a 29

		// boolean: valores booleanos (true/false)
		boolean minhaVariavelBoolean = false;

		System.out.println(minhaVariavelBoolean);

	}
}
